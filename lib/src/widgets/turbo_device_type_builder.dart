import 'package:flutter/widgets.dart';
import '../enums/turbo_device_type.dart';

class TurboDeviceTypeBuilder extends StatelessWidget {
  const TurboDeviceTypeBuilder({
    super.key,
    required this.child,
    required this.onDesktop,
    required this.onMobile,
    this.onTablet,
    this.deviceType,
  });

  final Widget Function(
    BuildContext context,
    TurboDeviceType deviceType,
    Widget child,
  )
  onDesktop;
  final Widget Function(
    BuildContext context,
    TurboDeviceType deviceType,
    Widget child,
  )
  onMobile;
  final Widget Function(
    BuildContext context,
    TurboDeviceType deviceType,
    Widget child,
  )?
  onTablet;
  final Widget child;
  final TurboDeviceType? deviceType;

  @override
  Widget build(BuildContext context) {
    // For now, use a default device type if not provided
    // In a real implementation, this would get device type from context or MediaQuery
    final deviceType = this.deviceType ?? TurboDeviceType.mobile;
    switch (deviceType) {
      case TurboDeviceType.mobile:
        return onMobile(context, deviceType, child);
      case TurboDeviceType.tablet:
        return onTablet?.call(context, deviceType, child) ??
            onDesktop(context, deviceType, child);
      case TurboDeviceType.desktop:
        return onDesktop(context, deviceType, child);
    }
  }
}
